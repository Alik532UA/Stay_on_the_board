<h1>Керування та гарячі клавіші</h1>

<h2>Ігрове керування</h2>
<h3>Вибір напрямку (NumPad)</h3>
<ul>
  <li><b>7</b> (↖): Вгору-ліворуч</li>
  <li><b>8</b> (↑): Вгору</li>
  <li><b>9</b> (↗): Вгору-праворуч</li>
  <li><b>4</b> (←): Ліворуч</li>
  <li><b>6</b> (→): Праворуч</li>
  <li><b>1</b> (↙): Вниз-ліворуч</li>
  <li><b>2</b> (↓): Вниз</li>
  <li><b>3</b> (↘): Вниз-праворуч</li>
</ul>
<p><b>Поведінка:</b></p>
<ul>
  <li>Перший натиск: вибір напрямку, автоматично вибирається відстань 1</li>
  <li>Той самий напрямок: збільшує відстань на 1 (до boardSize-1)</li>
  <li>На максимумі: наступний натиск скидає відстань на 1</li>
  <li>Інший напрямок: вибір нового напрямку, відстань 1 (якщо не вибрано вручну)</li>
</ul>

<h3>Альтернативне керування (QWE/ASD)</h3>
<ul>
  <li><b>Q</b> (↖): Вгору-ліворуч</li>
  <li><b>W</b> (↑): Вгору</li>
  <li><b>E</b> (↗): Вгору-праворуч</li>
  <li><b>A</b> (←): Ліворуч</li>
  <li><b>D</b> (→): Праворуч</li>
  <li><b>Z</b> (↙): Вниз-ліворуч</li>
  <li><b>S</b> або <b>X</b> (↓): Вниз</li>
  <li><b>C</b> (↘): Вниз-праворуч</li>
</ul>

<h3>Вибір відстані</h3>
<ul>
  <li><b>1-9</b>: Вибір конкретної відстані (до boardSize-1)</li>
  <li><b>Space</b>: Підтвердити хід (коли вибрано напрямок і відстань)</li>
  <li><b>Enter</b>: Підтвердити хід (альтернатива Space)</li>
</ul>
<p><b>Поведінка:</b> Вручну вибрана відстань зберігається при зміні напрямку. Автоматично вибрана — скидається.</p>

<h3>Підтвердження ходу</h3>
<ul>
  <li><b>Space</b>: Підтвердити хід</li>
  <li><b>Enter</b>: Підтвердити хід</li>
  <li><b>NumPad 5</b>: Підтвердити хід (центральна кнопка)</li>
  <li>Клік по центральному елементу: Підтвердити хід (коли вибрано напрямок і відстань)</li>
</ul>

<h3>Центральний елемент</h3>
<ul>
  <li>Показує поточний стан: напрямок, відстань, хід комп'ютера</li>
  <li>Клікабельний лише при виборі напрямку і відстані</li>
  <li>Візуальний фідбек: анімація, підсвітка</li>
</ul>

<h3>Ігрові дії</h3>
<ul>
  <li><b>Backspace</b>: Заявити "немає ходів"</li>
  <li><b>Escape</b>: Скасувати вибір</li>
  <li><b>H</b>: Показати довідку</li>
</ul>

<h2>Навігація</h2>
<ul>
  <li><b>Escape</b>: Повернутися в головне меню</li>
  <li><b>Tab</b>: Перехід між пунктами меню</li>
  <li><b>Enter</b>: Вибрати пункт меню</li>
</ul>

<h2>Налаштування</h2>
<ul>
  <li><b>T</b>: Перемкнути тему (світла/темна)</li>
  <li><b>L</b>: Перемкнути мову</li>
  <li><b>S</b>: Відкрити налаштування</li>
</ul>

<h2>Доступність</h2>
<ul>
  <li>Всі інтерактивні елементи мають ARIA-мітки</li>
  <li>Підтримується повна навігація з клавіатури</li>
  <li>Фокус та підсвітка видимі</li>
</ul>

<h2>Мобільна підтримка</h2>
<ul>
  <li>Тап по кнопках напрямку/відстані</li>
  <li>Тап по центральній кнопці для підтвердження</li>
  <li>Свайпи не підтримуються</li>
  <li>Адаптивний дизайн, великі touch-мішені</li>
</ul>

<h2>Поради</h2>
<ol>
  <li>Використовуйте одну клавішу напрямку для швидкого циклу відстаней</li>
  <li>Слідкуйте за центральною кнопкою для поточного вибору</li>
  <li>NumPad — найшвидший спосіб вибору напрямку</li>
  <li>QWE/ASD — зручне керування для гравців без NumPad</li>
  <li>Підтверджуйте хід Space/Enter після вибору напрямку і відстані</li>
  <li>На 3x3 дошці: 1 → 2 → 1 (цикл відстаней)</li>
  <li>Вручну вибрана відстань зберігається при зміні напрямку</li>
  <li>Автоматично вибрана відстань скидається при зміні напрямку</li>
</ol> 