  // –§—É–Ω–∫—Ü—ñ—è –æ—á–∏—â–µ–Ω–Ω—è –∫–µ—à—É
  function clearCache() {
    localStorage.clear();
    sessionStorage.clear();
    // –û—á–∏—â–µ–Ω–Ω—è –≤—Å—ñ—Ö cookies
    if (document.cookie && document.cookie !== '') {
      const cookies = document.cookie.split(';');
      for (let i = 0; i < cookies.length; i++) {
        const cookie = cookies[i];
        const eqPos = cookie.indexOf('=');
        const name = eqPos > -1 ? cookie.substr(0, eqPos) : cookie;
        document.cookie = name + '=;expires=Thu, 01 Jan 1970 00:00:00 GMT;path=/';
      }
    }
    location.reload();
  }
  let boardSizes = Array.from({length:8},(_,i)=>i+2); // number[]
  /** @type {number} */
  let boardSize;
  /** @type {number[][]} */
  let board;
  /** @type {number} */
  let playerRow;
  /** @type {number} */
  let playerCol;
  /** @type {{row:number,col:number}[]} */
  let availableMoves;
  /** @type {{row:number,col:number}[]} */
  let blockedCells;
  /** @type {boolean} */
  let blockModeEnabled;
  /** @type {number} */
  let currentPlayer;
  let showBoardSizeDropdown = false;
  function toggleBoardSizeDropdown() {
    showBoardSizeDropdown = !showBoardSizeDropdown;
  }
  function closeBoardSizeDropdown() {
    showBoardSizeDropdown = false;
  }
  /**
   * @param {number} n
   */
  function selectBoardSize(n) {
    setBoardSize(n);
    showBoardSizeDropdown = false;
  }

  $: boardSize = Number($appState.boardSize);
  $: board = $appState.board;
  $: playerRow = $appState.playerRow;
  $: playerCol = $appState.playerCol;
  $: availableMoves = $appState.availableMoves;
  $: blockedCells = $appState.blockedCells;
  $: blockModeEnabled = $appState.blockModeEnabled;
  $: currentPlayer = $appState.currentPlayer;
  $: showMoves = $appState.settings?.showMoves;
  $: showBoard = typeof $appState.settings?.showBoard === 'boolean' ? $appState.settings.showBoard : true;

  $: console.log('[GameBoard] playerRow:', playerRow, 'playerCol:', playerCol, 'availableMoves:', availableMoves, 'blockedCells:', blockedCells);
  // –í–∏–Ω–æ—à—É –≤–∏–∫–ª–∏–∫–∏ $_() –Ω–∞ –≤–µ—Ä—Ö–Ω—ñ–π —Ä—ñ–≤–µ–Ω—å
  $: playerTitle = $_('gameBoard.player');
  $: mainMenuTitle = $_('gameBoard.mainMenu');

  onMount(() => {
    if (!board || !Array.isArray(board) || board.length === 0) {
      logStore.addLog('–î–æ—à–∫–∞ –Ω–µ —ñ–Ω—ñ—Ü—ñ–∞–ª—ñ–∑–æ–≤–∞–Ω–∞, —Å—Ç–≤–æ—Ä—é—é –¥–µ—Ñ–æ–ª—Ç–Ω—É 3x3', 'warn');
      setBoardSize(3);
    }
  });

  function goToMainMenu() {
    logStore.addLog('–ü–æ–≤–µ—Ä–Ω–µ–Ω–Ω—è –¥–æ –≥–æ–ª–æ–≤–Ω–æ–≥–æ –º–µ–Ω—é', 'info');
    goto('/');
  }

  function onBoardSizeChange(/** @type {Event} */ event) {
    const newSize = +/** @type {HTMLSelectElement} */(event.target).value;
    logStore.addLog(`–ó–º—ñ–Ω–∞ —Ä–æ–∑–º—ñ—Ä—É –¥–æ—à–∫–∏ –Ω–∞ ${newSize}x${newSize}`, 'info');
    setBoardSize(newSize);
  }

  function isAvailable(/** @type {number} */ row, /** @type {number} */ col) {
    const result = availableMoves && availableMoves.some(move => move.row === row && move.col === col);
    console.log('[isAvailable] row:', row, 'col:', col, 'result:', result, 'availableMoves:', availableMoves);
    return result;
  }

  function isBlocked(row, col) {
    const result = blockedCells && blockedCells.some(cell => Number(cell.row) === Number(row) && Number(cell.col) === Number(col));
    console.log('[isBlocked]', {row, col, blockedCells, result});
    return result;
  }

  function onCellClick(/** @type {number} */ row, /** @type {number} */ col) {
    if (isAvailable(row, col) && !isBlocked(row, col)) {
      logStore.addLog(`–†—É—Ö –≥—Ä–∞–≤—Ü—è –Ω–∞ –∫–ª—ñ—Ç–∏–Ω–∫—É [${row},${col}]`, 'info');
      movePlayer(row, col);
      // –Ø–∫—â–æ —Ä–µ–∂–∏–º vsComputer ‚Äî —Ö—ñ–¥ –∫–æ–º–ø'—é—Ç–µ—Ä–∞ –ø—ñ—Å–ª—è –≥—Ä–∞–≤—Ü—è
      if ($appState.gameMode === 'vsComputer') {
        makeComputerMove();
      }
    }
  }

  function onCellRightClick(/** @type {Event} */ event, /** @type {number} */ row, /** @type {number} */ col) {
    event.preventDefault();
    if (blockModeEnabled && !(row === playerRow && col === playerCol)) {
      const blocked = blockedCells && blockedCells.some(cell => cell.row === row && cell.col === col);
      logStore.addLog(`${blocked ? '–†–æ–∑–±–ª–æ–∫—É–≤–∞–Ω–Ω—è' : '–ë–ª–æ–∫—É–≤–∞–Ω–Ω—è'} –∫–ª—ñ—Ç–∏–Ω–∫–∏ [${row},${col}]`, 'info');
      toggleBlockCell(row, col);
    }
  }
</script>

